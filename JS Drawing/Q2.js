//Write a JavaScript program to draw a circle.

var circle = document.createElement('div');

// Apply styles to the circle
circle.style.width = '300px';    // Set width to 300px
circle.style.height = '300px';   // Set height to 100px
circle.style.backgroundColor = 'white';  // Set background color to white
circle.style.border = '1px solid red';

// Append the circle to the document body
document.body.appendChild(circle);