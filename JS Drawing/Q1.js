//Write a JavaScript program to draw the following rectangular shape.
// Create a div element for the rectangle
var rectangle = document.createElement('div');

// Apply styles to the rectangle
rectangle.style.width = '300px';    // Set width to 300px
rectangle.style.height = '100px';   // Set height to 100px
rectangle.style.backgroundColor = 'white';  // Set background color to white
rectangle.style.border = '2px solid black';  // Set border to 2px solid black

// Append the rectangle to the document body
document.body.appendChild(rectangle);
